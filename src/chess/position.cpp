void Position::to_fen(Span<char> fen) const
{
	size_t n = 0;
	size_t index = 0;
	Square i = SQ_END - 1;
	for (; i >= SQ_START; --i)
	{
		if (i % 9 == 8 && i != SQ_END - 1)
		{
			if (n != 0)
			{
				fen[index++] = narrowing_static_cast<char>('0' + n);
				n = 0;
			}
			fen[index++] = '/';
		}

		if (m_squares[i] != PIECE_NONE)
		{
			if (n != 0)
			{
				fen[index++] = narrowing_static_cast<char>('0' + n);
				n = 0;
			}
			fen[index++] = piece_to_char(m_squares[i]);
		}
		else
			++n;
	}

	if (i == -1 && n != 0)
		fen[index++] = narrowing_static_cast<char>('0' + n);

	fen[index++] = ' ';
	fen[index++] = m_turn == WHITE ? 'w' : 'b';

	fen[index] = '\0';
}

void Position::from_fen(Const_Span<char> fen)
{
	clear();

	const size_t len = fen.size();
	Square sq = SQ_END - 1;
	size_t i = 0;
	for (; i < len && sq >= SQ_START; ++i)
	{
		if (std::isdigit(fen[i]))
			sq -= fen[i] - '0';
		else if (std::isalpha(fen[i]))
		{
			const Piece piece = piece_from_char(fen[i]);
			if (piece != PIECE_NONE)
				put_piece(piece, sq);
			sq -= 1;
		}
	}

	if (sq == -1)
	{
		for (; i < len; ++i)
		{
			if (fen[i] == 'W' || fen[i] == 'w' || fen[i] == 'R' || fen[i] == 'r')
			{
				m_turn = WHITE;
				break;
			}
			else if (fen[i] == 'B' || fen[i] == 'b')
			{
				m_turn = BLACK;
				break;
			}
		}
	}
	else
	{
		m_turn = WHITE;
	}
}


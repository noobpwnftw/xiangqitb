void Position::display() const
{
	static const char* PieceStr[16] =
	{
		"","帅","车","马","炮","士","相","兵",
		"","将","俥","傌","垉","仕","象","卒",
	};

	printf("\n");

	for (Square i = SQ_END - 1; i >= SQ_START; --i)
	{
		const File file = sq_file(i);
		const Rank rank = sq_rank(i);
		const Piece piece = m_squares[i];

		if (file == FILE_I)
			printf(" ");

		if (piece != PIECE_NONE)
			printf("%s", PieceStr[piece]);
		else if (i == SQ_I9)
			printf("┌");
		else if (i == SQ_A9)
			printf("┐");
		else if (i == SQ_I0)
			printf("└");
		else if (i == SQ_A0)
			printf("┘");
		else if (file == FILE_I)
			printf("├");
		else if (file == FILE_A)
			printf("┤");
		else if (rank == RANK_4 || rank == RANK_9)
			printf("┬");
		else if (rank == RANK_0 || rank == RANK_5)
			printf("┴");
		else
			printf("┼");

		if (file == FILE_A)
		{
			printf(" %d\n", static_cast<int>(rank));
			if (rank == RANK_5)
				printf(" │                              │\n");
			else if (rank == RANK_2 || rank == RANK_9)
				printf(" │  │  │  │╲│╱│  │  │  │\n");
			else if (rank == RANK_1 || rank == RANK_8)
				printf(" │  │  │  │╱│╲│  │  │  │\n");
			else if (rank != RANK_0)
				printf(" │  │  │  │  │  │  │  │  │\n");
		}
		else
			printf("─");
	}

	printf("\n Ａ  Ｂ  Ｃ  Ｄ  Ｅ  Ｆ  Ｇ  Ｈ  Ｉ\n");

	char string[MAX_FEN_LENGTH];
	to_fen(Span(string));
	printf("fen : %s\n\n", string);
}

Piece Position::do_capture_move(Move move)
{
	const Square from = move.from();
	const Square to = move.to();
	const Piece cap = m_squares[to];

	m_pieces[m_squares[from]] ^= from ^ to;
	m_pieces[piece_occupy(m_turn)] ^= from ^ to;
	m_pieces[cap] ^= to;
	m_pieces[piece_occupy(color_opp(m_turn))] ^= to;
	m_occupied ^= from;
	m_piece_counts[cap] -= 1;

	m_squares[to] = m_squares[from];
	m_squares[from] = PIECE_NONE;

	m_turn = color_opp(m_turn);

	return cap;
}

void Position::undo_capture_move(Move move, Piece cap)
{
	const Square from = move.from();
	const Square to = move.to();

	m_turn = color_opp(m_turn);

	m_pieces[m_squares[to]] ^= from ^ to;
	m_pieces[piece_occupy(m_turn)] ^= from ^ to;
	m_pieces[cap] ^= to;
	m_pieces[piece_occupy(color_opp(m_turn))] ^= to;
	m_occupied ^= from;
	m_piece_counts[cap] += 1;

	m_squares[from] = m_squares[to];
	m_squares[to] = cap;
}

void Position::do_quiet_move(Move move)
{
	const Square from = move.from();
	const Square to = move.to();
	ASSERT(m_squares[from]);

	m_pieces[m_squares[from]] ^= from ^ to;
	m_pieces[piece_occupy(m_turn)] ^= from ^ to;
	m_occupied ^= from ^ to;

	m_squares[to] = m_squares[from];
	m_squares[from] = PIECE_NONE;

	m_turn = color_opp(m_turn);
}

void Position::undo_quiet_move(Move move)
{
	const Square from = move.from();
	const Square to = move.to();
	ASSERT(m_squares[to]);

	m_turn = color_opp(m_turn);

	m_pieces[m_squares[to]] ^= from ^ to;
	m_pieces[piece_occupy(m_turn)] ^= from ^ to;
	m_occupied ^= from ^ to;

	m_squares[from] = m_squares[to];
	m_squares[to] = PIECE_NONE;
}

